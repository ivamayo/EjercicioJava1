package tipodatos;

public class TipoDato {

    //int
    public static void DatoInt(int valor)
    {
        System.out.println("El valor numero es " + valor);
    }

    public static void DatoString( String valor)
    {
        System.out.println("El valor string es " + valor);
    }

    public static void DatoLong(long valor)
    {
        System.out.println("El valor long es "+ valor);
    }

    public static void DatoDouble(double valor)
    {
        System.out.println("El valor double es "+ valor);
    }

    public static void DatoBoolean(boolean valor){
        System.out.println("El valor boolean es " + valor);
    }


}
